//! Command registry — single source of truth for CLI commands, API paths, and form fields.
//!
//! Both the Rust CLI and the web terminal consume this registry.
//! Adding a command here automatically makes it available everywhere.

use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CommandManifest {
    pub version: String,
    pub commands: Vec<CommandDef>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CommandDef {
    /// CLI command path: "form create", "cap-table issue", etc.
    pub cli_path: String,
    /// Short description shown in help and autocomplete.
    pub description: String,
    /// HTTP method: "GET", "POST", "PUT", "PATCH", or "DELETE".
    pub method: String,
    /// URL path template with {entity_id} placeholder.
    /// Example: "/v1/formations/{entity_id}/advance"
    pub url: String,
    /// Whether this command requires an active entity context.
    pub needs_entity: bool,
    /// Category for grouping in help output.
    pub category: String,
    /// Fields the user must provide (for POST/PUT/PATCH commands).
    pub fields: Vec<FieldDef>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FieldDef {
    /// API body field name (snake_case): "legal_name", "entity_type"
    pub name: String,
    /// Human-readable label for forms: "Legal name"
    pub label: String,
    /// Field type: "text", "number", "enum", "date", "bool", "json"
    pub field_type: String,
    /// Whether this field is required.
    pub required: bool,
    /// For enum fields, the valid values.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub enum_values: Option<Vec<String>>,
    /// Placeholder or hint text.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub placeholder: Option<String>,
    /// Default value if any.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub default_value: Option<String>,
}

/// Build the complete command manifest from the registry.
pub fn build_manifest() -> CommandManifest {
    CommandManifest {
        version: env!("CARGO_PKG_VERSION").to_string(),
        commands: all_commands(),
    }
}

